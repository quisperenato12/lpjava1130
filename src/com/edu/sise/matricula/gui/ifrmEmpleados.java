    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        boolean rpta;
        int op=-1;

        //pedir una reeconfirmacion

        op=JOptionPane.showConfirmDialog(this, "DESEA REALMENTE ALGUN REGISTRO", "SISTEMA", JOptionPane.YES_NO_OPTION);
        if(op==JOptionPane.YES_OPTION){
            rpta=logica.eliminarEmpleado(Integer.parseInt(txtid.getText()));

            if(rpta){
                JOptionPane.showMessageDialog(null, "SE ELIMINO CORRECTAMENTE :) !!!!");
                txtid.setText("");
                txtdni.setText("");
                txtnombres.setText("");
                txtsueldo.setText("");
                txtapellidos.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "NO SE PUDO ELIMINAR :) !!!!");
            }
        }
        logica.imprimir(tbLista);
    }//GEN-LAST:event_btneliminarActionPerformed

