    private void tbListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaMouseClicked
        // TODO add your handling code here:
        //primero quiero saber que fila ha sido seleccionada
      int fila= -1;
       int id= -1;
       double sueldo= -1;
        
        String dni="";
        String nombres="";
        String apellidos="";

        fila=tbLista.getSelectedRow();
        if(fila>=0){
            id=Integer.parseInt(String.valueOf(tbLista.getValueAt(fila, 0)));
            dni=String.valueOf(tbLista.getValueAt(fila, 1));
            nombres=String.valueOf(tbLista.getValueAt(fila, 2));
            apellidos=String.valueOf(tbLista.getValueAt(fila, 3));
            sueldo=Double.parseDouble(String.valueOf(tbLista.getValueAt(fila, 4)));
        }
        txtid.setText(id+"");
        txtdni.setText(dni+"");
        txtnombres.setText(nombres);
        txtapellidos.setText(apellidos);
        txtsueldo.setText(sueldo+"");


    }//GEN-LAST:event_tbListaMouseClicked

